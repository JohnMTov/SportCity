package com.example.sportcity;

public class Sport {
    private int id;
    private int img;
    private String title;

    public Sport(int id, int img, String title) {
        this.id = id;
        this.img = img;
        this.title = title;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

}
